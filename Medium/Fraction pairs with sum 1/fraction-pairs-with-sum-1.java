class Solution {
    public static int countFractions(int l, int[] n, int[] d) {
        // code here
        Map<Double,Integer> map=new HashMap<>();
        int i,j;
        int ans=0;
        for(i=0;i<l;i++)
        {
            double dd=(double)((double)n[i]/(double)d[i]);
            double temp=(double)((double)(d[i]-n[i])/(double)d[i]);
            ans+=map.getOrDefault(temp,0);
            map.put(dd,map.getOrDefault(dd,0)+1);
        }
        return ans;
    }
}
        
