class Solution{
    public static long distinctColoring(int n, int[]red, int[]g, int[]b){
        //code here
        long x=red[0],y=g[0],z=b[0];
        int i;
        for(i=1;i<n;i++)
        {
            long p=x,q=y,r=z;
            x=red[i]+Math.min(q,r);
            y=g[i]+Math.min(p,r);
            z=b[i]+Math.min(p,q);
        }
        return Math.min(x,Math.min(y,z));
    }
}


