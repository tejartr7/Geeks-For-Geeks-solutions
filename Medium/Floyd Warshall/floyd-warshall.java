class Solution
{
    public void shortest_distance(int[][] dist)
    {
        // Code here 
        int i,j,k;
        int n=dist.length;
        for(i=0;i<n;i++)
        {
            for(j=0;j<n;j++)
            {
                for(k=0;k<n;k++)
                {
                    if(dist[j][i]==-1 || dist[i][k]==-1)
                    continue;
                    if(dist[j][k]==-1 || dist[j][k]>dist[j][i]+dist[i][k])
                    {
                        dist[j][k]=dist[j][i]+dist[i][k];
                    }
                }
            }
        }
    }
}